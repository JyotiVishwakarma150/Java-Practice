package Java_Basics;

public class Escape_01 {
    public static void main(String[] args) {
        System.out.println("Hello\"KGCoding\"...");
        System.out.println("Hello\\KGCoding\\...");
        System.out.println("Hello \nKGCoding...");
        System.out.println("Hello \tKGCoding...");
        System.out.println("Hello\bKGCoding...");
    }
}
